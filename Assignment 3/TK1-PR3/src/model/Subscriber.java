package model;

import java.util.Set;

/**
 *The model for a subscriber. He subscribes to a list of tags and a list of
 * names (publishers).
 * @author Satia Herfert
 * @author Dipika Gupta
 * @author Anadi Tyagi
 * @author Floriment Klinaku
 */
public class Subscriber {

    private Set<String> tags;
    private Set<String> names;

    public Subscriber(Set<String> tags, Set<String> names) {
        this.tags = tags;
        this.names = names;
    }

    public Set<String> getTags() {
        return tags;
    }

    public void setTags(Set<String> tags) {
        this.tags = tags;
    }

    public Set<String> getNames() {
        return names;
    }

    public void setNames(Set<String> names) {
        this.names = names;
    }
}
